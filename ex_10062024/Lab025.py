# Literals
# var_name = variable_value
name = "Usha"
print(name)
print(type(name))
print(len(name))
# var_name -> Identifier
# var_value -> Literals
# Literals are the actual values assigned
# Literals can be Numeric and Non-Numeric
age = 12
print(age)
print(type(age))
# print(len(age)) -> TypeError: object of type 'int' has no len()
pi = 3.14
print(pi)
print(type(pi))
is_Married = True
print(is_Married)
print(type(is_Married))
have_lambo = None
print(have_lambo)
print(type(have_lambo))
my_list = []
print(my_list)
print(len(my_list))
my_set = {}
